package com.imsweb.seerapi.client.naaccr;

public class NaaccrItemInfo {

    private String id;
    private String item;
    private String name;

    public NaaccrItemInfo() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getItem() {
        return item;
    }

    public void setItem(String item) {
        this.item = item;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}